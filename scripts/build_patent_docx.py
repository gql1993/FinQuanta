def _set_run_font(run, size_pt: int | None = None, bold: bool = False):
    run.bold = bold
    if size_pt is not None:
        run.font.size = Pt(size_pt)
    run.font.name = "宋体"
    r = run._element
    rPr = r.get_or_add_rPr()
    rFonts = OxmlElement("w:rFonts")
    rFonts.set(qn("w:eastAsia"), "宋体")
    rPr.insert(0, rFonts)


def _body_para(doc: Document, text: str):
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Pt(21)
    p.paragraph_format.line_spacing_rule = WD_LINE_SPACING.MULTIPLE
    p.paragraph_format.line_spacing = 1.5
    run = p.add_run(text)
    _set_run_font(run, size_pt=12, bold=False)
    return p


def _heading(doc: Document, text: str, size_pt: int):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(6)
    p.paragraph_format.space_after = Pt(6)
    run = p.add_run(text)
    _set_run_font(run, size_pt=size_pt, bold=True)
    return p


